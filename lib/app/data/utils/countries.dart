class Country {
  String name;
  String code;
  String isoCode;

  Country({
    required this.name,
    required this.code,
    required this.isoCode,
  });

  @override
  String toString() {
    return "Country(name: '$name', code: '$code', isoCode: '$isoCode')\n";
  }

  Map<String, dynamic> toMap() {
    return {
      'name': name,
      'dialCode': code,
      'isoCode': isoCode,
    };
  }

  factory Country.fromMap(Map<String, dynamic> map) {
    return Country(
      name: map['name'] as String,
      code: map['dialCode'] as String,
      isoCode: map['isoCode'] as String,
    );
  }
}

