class Address {
  final int id;
  final String streetAddress;
  final String pinCode;
  final String city;
  final String district;
  final String state;
  final String landMark;

  Address({
    required this.id,
    required this.streetAddress,
    required this.city,
    required this.district,
    required this.state,
    required this.pinCode,
    required this.landMark,
  });
}
